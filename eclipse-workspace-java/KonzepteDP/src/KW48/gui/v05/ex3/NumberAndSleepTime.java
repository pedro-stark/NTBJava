package KW48.gui.v05.ex3;

class NumberAndSleepTime {
    private int number;
    private long sleepTime;

    public NumberAndSleepTime(int number, long sleepTime) {
        this.number = number;
        this.sleepTime = sleepTime;
    }

    public int getNumber() {
        return number;
    }

    public long getSleepTime() {
        return sleepTime;
    }    
}
